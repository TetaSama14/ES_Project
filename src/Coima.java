public class Coima {

    String codCoima;
    Integer valor;

    public Coima(String codCoima, Integer valor) {
        this.codCoima = codCoima;
        this.valor = valor;
    }

    public String getCodCoima() {
        return codCoima;
    }

    public void setCodCoima(String codCoima) {
        this.codCoima = codCoima;
    }

    public Integer getValor() {
        return valor;
    }

    public void setValor(Integer valor) {
        this.valor = valor;
    }
}

